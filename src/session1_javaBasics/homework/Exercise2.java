package session1_javaBasics.homework;
/*
 * 2. Using Variables: Write a program that defines four different variables (an integer,
 * a double, a character, and a string), assigns them values, and then prints them out.
 */
public class Exercise2 {
    public static void main(String[] args) {
        //declaration of variables
        int anInteger = 32000;
        double aDouble = 4445555.6666;
        char aChar = 'D';
        String aString = "A random string";

        //printing the results
        System.out.println("An integer: " + anInteger);
        System.out.println("A double: " + aDouble);
        System.out.println("A characte: " + aChar);
        System.out.println("A string: " + aString);
    }
}
